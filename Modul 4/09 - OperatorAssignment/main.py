# Operasi yang dapat dilakukan dengan meringkas

a = 5 # adalah assignment
print("niali a = ", a)

a += 1 # artinya a = a + 1
print("nilai a =+ 1, nilai a menjadi ", a) 

a -= 1 # artinya a = a - 1
print("nilai a -+ 1, nilai a menjadi ", a)

a *= 5 # artinya a = a * 5
print("nilai a *+ 5, nilai a menjadi ", a)

a /= 2 # artinya a = a / 2
print("nilai a /+ 2, nilai a menjadi ", a)

# Modulus dan floor division

b = 10 
print("nilai b = ", b)

b %= 3
print("nilai b = menjadi = ", b)

b = 10 
print("nilai b menjadi = ", b)

b //= 3
print("nilai b menjadi = ", b)

# Eksponen/pangkat 
a = 5
print("nilai a =", a)
a **= 3
print("nilai a menjadi = ", a)

